    public void leituraTabela() {

        DefaultTableModel dtmCliente = (DefaultTableModel) tabCliente.getModel();
        dtmCliente.setNumRows(0);
        ClienteDAO objCdao = new ClienteDAO();

        for (Cliente cli : objCdao.Leitura()) {

            dtmCliente.addRow(new Object[]{
                cli.getIdCliente(),
                cli.getNomeCliente(),
                cli.getCpfCliente(),
                cli.getEndCliente(),
                cli.getBaiCliente(),
                cli.getCidCliente(),
                cli.getUfCliente(),
                cli.getCepCliente(),
                cli.getTelCliente(),
                cli.getEmCliente(),});

        }

    }

    public void leituraTabPesquisar(String nomeCliente) {

        DefaultTableModel dtmCliente = (DefaultTableModel) tabCliente.getModel();
        dtmCliente.setNumRows(0);
        ClienteDAO objCdao = new ClienteDAO();

        for (Cliente cli : objCdao.Pesquisar(nomeCliente)) {

            dtmCliente.addRow(new Object[]{
                cli.getIdCliente(),
                cli.getNomeCliente(),
                cli.getCpfCliente(),
                cli.getEndCliente(),
                cli.getBaiCliente(),
                cli.getCidCliente(),
                cli.getUfCliente(),
                cli.getCepCliente(),
                cli.getTelCliente(),
                cli.getEmCliente(),});

        }

    }

