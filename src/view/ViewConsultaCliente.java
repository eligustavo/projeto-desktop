    private void txtExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtExcluirActionPerformed
        // TODO add your handling code here:

        if (tabCliente.getSelectedRow() != -1) {

            Cliente objcliente = new Cliente();
            ClienteDAO dao = new ClienteDAO();

            objcliente.setIdCliente((int) tabCliente.getValueAt(tabCliente.getSelectedRow(), 0));
            dao.Excluir(objcliente);

            txtNomeCli.setText("");
            txtCpf.setText("");
            txtEnd.setText("");
            txtBai.setText("");
            txtCid.setText("");
            txtCep.setText("");
            txtTele.setText("");
            txtEm.setText("");

            leituraTabela();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione um cliente para excluir!");
        }

    }//GEN-LAST:event_txtExcluirActionPerformed

    private void txtAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtAlterarActionPerformed
        // TODO add your handling code here

        if (tabCliente.getSelectedRow() != -1) {

            Cliente objcliente = new Cliente();
            ClienteDAO dao = new ClienteDAO();
            objcliente.setNomeCliente(txtNomeCli.getText());
            objcliente.setCpfCliente(txtCpf.getText());
            objcliente.setCpfCliente(txtCpf.getText());
            objcliente.setEndCliente(txtEnd.getText());
            objcliente.setBaiCliente(txtBai.getText());
            objcliente.setCidCliente(txtCid.getText());
            objcliente.setUfCliente(txtUF.getSelectedItem().toString());
            objcliente.setCepCliente(txtCep.getText());
            objcliente.setTelCliente(txtTele.getText());
            objcliente.setEmCliente(txtEm.getText());
            objcliente.setIdCliente((int) tabCliente.getValueAt(tabCliente.getSelectedRow(), 0));
            dao.Alterar(objcliente);

            txtNomeCli.setText("");
            txtCpf.setText("");
            txtEnd.setText("");
            txtBai.setText("");
            txtCid.setText("");
            txtUF.setSelectedItem("");
            txtCep.setText("");
            txtTele.setText("");
            txtEm.setText("");

            leituraTabela();

        } else {
            JOptionPane.showMessageDialog(null, "Selecione cliente para atualizar");
        }


    }//GEN-LAST:event_txtAlterarActionPerformed

